// --- setServerProperty ---

func TestSetServerPropertyUpdatesExisting(t *testing.T) {
	lines := []string{"# comment", "server-port=25565", "max-players=20"}
	lines = setServerProperty(lines, "server-port", "25566")
	if lines[1] != "server-port=25566" {
		t.Fatalf("expected server-port=25566, got %q", lines[1])
	}
	if len(lines) != 3 {
		t.Fatalf("expected 3 lines, got %d", len(lines))
	}
}

func TestSetServerPropertyAppendsWhenMissing(t *testing.T) {
	lines := []string{"# comment", "max-players=20"}
	lines = setServerProperty(lines, "server-port", "25565")
	last := lines[len(lines)-1]
	if last != "server-port=25565" {
		t.Fatalf("expected appended entry, got %q", last)
	}
}

func TestSetServerPropertyPreservesComments(t *testing.T) {
	lines := []string{"#Generated by Minecraft", "server-port=25565"}
	lines = setServerProperty(lines, "server-port", "25566")
	if lines[0] != "#Generated by Minecraft" {
		t.Fatalf("comment was destroyed: %q", lines[0])
	}
}

func TestSetServerPropertyCaseInsensitiveKey(t *testing.T) {
	lines := []string{"Server-Port=25565"}
	lines = setServerProperty(lines, "server-port", "25566")
	if lines[0] != "server-port=25566" {
		t.Fatalf("expected updated line, got %q", lines[0])
	}
}

