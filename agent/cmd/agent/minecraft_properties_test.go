// helpers

func mustReadFile(t *testing.T, path string) string {
	t.Helper()
	b, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("ReadFile %s: %v", path, err)
	}
	return string(b)
}

func mustWriteFile(path, content string) {
	if err := os.WriteFile(path, []byte(content), 0o640); err != nil {
		panic(err)
	}
}

// assertProperty checks that the file content contains "key=value".
func assertProperty(t *testing.T, content, key, value string) {
	t.Helper()
	want := key + "=" + value
	for _, line := range strings.Split(content, "\n") {
		if strings.TrimSpace(line) == want {
			return
		}
	}
	t.Fatalf("expected %q in server.properties:\n%s", want, content)
}
